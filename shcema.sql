	id serial4 NOT NULL,
	item_id text NOT NULL,
	"source" text NOT NULL,
	city text NULL,
	url text NULL,
	title text NULL,
	price int4 NULL,
	published_at timestamp NULL,
	address text NULL,
	metro _text NULL,
	description text NULL,
	property_type text NULL,
